  def resolve_discriminant_info(union)
    dtype = union.discriminant.type
    disc_field_name = union.discriminant.name.to_s

    if dtype.respond_to?(:resolved_type)
      resolved = dtype.resolved_type
      if resolved.is_a?(AST::Definitions::Enum)
        php_name = name(resolved)
        return { kind: :enum, php_name: php_name, enum_defn: resolved, field_name: disc_field_name }
      end
    end

    { kind: :int, php_name: nil, enum_defn: nil, field_name: disc_field_name }
  end

  # ---------------------------------------------------------------------------
  # Union arm building
  # ---------------------------------------------------------------------------

  def build_union_arms(union, union_name, disc_info)
    arms = []
    seen_fields = Set.new

    union.normal_arms.each do |arm|
      if arm.void?
        labels = arm.cases.map { |c| format_case_label(c.value, disc_info) }
        arms << {
          case_labels: labels,
          void: true,
          is_default: false,
        }
      else
        field_name = resolve_field_name(union_name, arm.name)
        next if seen_fields.include?(field_name)
        seen_fields.add(field_name)

        labels = arm.cases.map { |c| format_case_label(c.value, disc_info) }
        arm_info = resolve_php_arm_info(arm, union_name)

        arms << {
          case_labels: labels,
          void: false,
          field_name: field_name,
          php_type: arm_info[:php_type],
          encode_style: arm_info[:encode_style],
          decode_style: arm_info[:decode_style],
          element_type: arm_info[:element_type],
          element_typespec: arm_info[:element_typespec],
          inner_type: arm_info[:inner_type],
          inner_typespec: arm_info[:inner_typespec],
          fixed_size: arm_info[:fixed_size],
          typespec: arm_info[:typespec],
          is_default: false,
        }
      end
    end

    # Default arm
    if union.default_arm.present?
      da = union.default_arm
      if da.void?
        arms << {
          case_labels: ["default"],
          void: true,
          is_default: true,
        }
      else
        field_name = resolve_field_name(union_name, da.name)
        arm_info = resolve_php_arm_info(da, union_name)
        arms << {
          case_labels: ["default"],
          void: false,
          field_name: field_name,
          php_type: arm_info[:php_type],
          encode_style: arm_info[:encode_style],
          decode_style: arm_info[:decode_style],
          element_type: arm_info[:element_type],
          element_typespec: arm_info[:element_typespec],
          inner_type: arm_info[:inner_type],
          inner_typespec: arm_info[:inner_typespec],
          fixed_size: arm_info[:fixed_size],
          typespec: arm_info[:typespec],
          is_default: true,
        }
      end
    end

    arms
  end

  def format_case_label(value, disc_info)
    if value.is_a?(AST::Identifier)
      if disc_info[:kind] == :enum
        "#{disc_info[:php_name]}::#{value.name}"
      else
        value.name.to_s
      end
    else
      value.value.to_s
    end
  end

  def resolve_php_arm_info(arm, union_name)
    decl = arm.declaration

    case decl
    when AST::Declarations::Array
      element_typespec = decl.type
      element_type = php_type_for_typespec(element_typespec)
      {
        php_type: "array",
        encode_style: :array,
        decode_style: :array,
        element_type: element_type,
        element_typespec: element_typespec,
        inner_type: nil,
        fixed_size: nil,
        typespec: nil,
      }
    when AST::Declarations::Optional
      inner_typespec = decl.type
      inner_type = php_type_for_typespec(inner_typespec)
      {
        php_type: inner_type,
        encode_style: :optional,
        decode_style: :optional,
        element_type: nil,
        element_typespec: nil,
        inner_type: inner_type,
        inner_typespec: inner_typespec,
        fixed_size: nil,
        typespec: nil,
      }
    when AST::Declarations::String
      {
        php_type: "string",
        encode_style: :string,
        decode_style: :string,
        element_type: nil,
        element_typespec: nil,
        inner_type: nil,
        fixed_size: nil,
        typespec: nil,
      }
    when AST::Declarations::Opaque
      if decl.fixed?
        size = resolve_size(decl)
        {
          php_type: "string",
          encode_style: :opaque_fixed,
          decode_style: :opaque_fixed,
          element_type: nil,
          element_typespec: nil,
          inner_type: nil,
          fixed_size: size,
          typespec: nil,
        }
      else
        {
          php_type: "string",
          encode_style: :opaque_var,
          decode_style: :opaque_var,
          element_type: nil,
          element_typespec: nil,
          inner_type: nil,
          fixed_size: nil,
          typespec: nil,
        }
      end
    else
      arm_typespec = decl.respond_to?(:type) ? decl.type : nil
      type_str = php_type_for_typespec(arm_typespec)
      {
        php_type: type_str,
        encode_style: :simple,
        decode_style: :simple,
        element_type: nil,
        element_typespec: nil,
        inner_type: nil,
        fixed_size: nil,
        typespec: arm_typespec,
      }
    end
  end

  # ---------------------------------------------------------------------------
  # XDR-to-PHP type mapping
  # ---------------------------------------------------------------------------

  def php_type_for_typespec(type)
    case type
    when AST::Typespecs::Bool
      "bool"
    when AST::Typespecs::Int
      "int"
    when AST::Typespecs::UnsignedInt
      "int"
    when AST::Typespecs::Hyper
      "int"
    when AST::Typespecs::UnsignedHyper
      "int"
    when AST::Typespecs::Float
      raise "XDR float type not supported by PHP SDK (no XdrEncoder::float method)"
    when AST::Typespecs::Double
      raise "XDR double type not supported by PHP SDK (no XdrEncoder::double method)"
    when AST::Typespecs::Quadruple
      raise "quadruple not supported in PHP"
    when AST::Typespecs::String
      "string"
    when AST::Typespecs::Opaque
      "string"
    when AST::Typespecs::Simple
      resolved = type.resolved_type
      resolved_name = name(resolved)
      # Check TYPE_OVERRIDES first
      if TYPE_OVERRIDES.key?(resolved_name)
        return TYPE_OVERRIDES[resolved_name]
      end
      # For SELF_REFERENCING_BASE_TYPES, return the wrapper class name
      if BASE_WRAPPER_TYPES.include?(resolved_name) && SELF_REFERENCING_BASE_TYPES.include?(resolved_name)
        return resolved_name
      end
      # For typedefs, use the PHP wrapper class name
      if resolved.is_a?(AST::Definitions::Typedef)
        underlying = resolved.declaration.type
        if underlying.sub_type == :optional
          return php_type_for_typespec(underlying)
        end
      end
      resolved_name
    when AST::Definitions::Base
      name(type)
    when AST::Concerns::NestedDefinition
      name(type)
    else
      raise "Unknown type reference: #{type.class.name}"
    end
  end

  def php_type_string(decl, member = nil)
    is_optional = member && (member.type.sub_type == :optional || typedef_is_optional?(decl.type))

    case decl
    when AST::Declarations::Array
      "array"
    when AST::Declarations::Opaque
      "string"
    when AST::Declarations::String
      is_optional ? "?string" : "string"
    else
      base = php_type_for_typespec(decl.type)
      is_optional ? "?#{base}" : base
    end
  end

  # ---------------------------------------------------------------------------
  # Encode/decode call generators
  # ---------------------------------------------------------------------------

  # Primitive XDR typespecs that require precise encoding — all map to PHP "int"
  # but use different XdrEncoder/XdrBuffer methods. Lazily resolved to avoid
  # referencing AST classes before xdrgen loads them.
  def primitive_typespec?(typespec)
    typespec.is_a?(AST::Typespecs::Int) ||
      typespec.is_a?(AST::Typespecs::UnsignedInt) ||
      typespec.is_a?(AST::Typespecs::Hyper) ||
      typespec.is_a?(AST::Typespecs::UnsignedHyper) ||
      typespec.is_a?(AST::Typespecs::Bool) ||
      typespec.is_a?(AST::Typespecs::String)
  end

  # When typespec is provided and is a primitive, use the precision-preserving
  # encode_call_for_typespec path instead of the lossy PHP-type-string dispatch.
  def encode_type_call(php_type, accessor, typespec: nil)
    if typespec && primitive_typespec?(typespec)
      return encode_call_for_typespec(typespec, accessor)
    end
    case php_type
    when "bool"
      "XdrEncoder::boolean(#{accessor})"
    when "string"
      "XdrEncoder::string(#{accessor})"
    else
      "#{accessor}->encode()"
    end
  end

  # When typespec is provided and is a primitive, use the precision-preserving
  # decode_call_for_typespec path instead of the lossy PHP-type-string dispatch.
  def decode_type_call(php_type, typespec: nil)
    if typespec && primitive_typespec?(typespec)
      return decode_call_for_typespec(typespec)
    end
    case php_type
    when "bool"
      "$xdr->readBoolean()"
    when "string"
      "$xdr->readString()"
    else
      "#{php_type}::decode($xdr)"
    end
  end

  # More precise encode call that respects the XDR type (int vs uint vs hyper etc.)
  def encode_call_for_typespec(typespec, accessor)
    case typespec
    when AST::Typespecs::Int
      "XdrEncoder::integer32(#{accessor})"
    when AST::Typespecs::UnsignedInt
      "XdrEncoder::unsignedInteger32(#{accessor})"
    when AST::Typespecs::Hyper
      "XdrEncoder::integer64(#{accessor})"
    when AST::Typespecs::UnsignedHyper
      "XdrEncoder::unsignedInteger64(#{accessor})"
    when AST::Typespecs::Bool
      "XdrEncoder::boolean(#{accessor})"
    when AST::Typespecs::String
      "XdrEncoder::string(#{accessor})"
    else
      encode_type_call(php_type_for_typespec(typespec), accessor)
    end
  end

  def decode_call_for_typespec(typespec)
    case typespec
    when AST::Typespecs::Int
      "$xdr->readInteger32()"
    when AST::Typespecs::UnsignedInt
      "$xdr->readUnsignedInteger32()"
    when AST::Typespecs::Hyper
      "$xdr->readInteger64()"
    when AST::Typespecs::UnsignedHyper
      "$xdr->readUnsignedInteger64()"
    when AST::Typespecs::Bool
      "$xdr->readBoolean()"
    when AST::Typespecs::String
      "$xdr->readString()"
    else
      decode_type_call(php_type_for_typespec(typespec))
    end
  end

  # ---------------------------------------------------------------------------
  # toBase64Xdr / fromBase64Xdr methods
  # ---------------------------------------------------------------------------

  def render_base64_methods(out)
    out.puts "    public function toBase64Xdr(): string {"
    out.puts "        return base64_encode($this->encode());"
    out.puts "    }"
    out.puts ""
    out.puts "    public static function fromBase64Xdr(string $xdr): static {"
    out.puts "        $decoded = base64_decode($xdr, true);"
    out.puts "        if ($decoded === false) {"
    out.puts "            throw new \\InvalidArgumentException('Invalid base64-encoded XDR');"
    out.puts "        }"
    out.puts "        return static::decode(new XdrBuffer($decoded));"
    out.puts "    }"
  end

  # ---------------------------------------------------------------------------
  # Name resolution
  # ---------------------------------------------------------------------------

  def name(named)
    raw_xdr_name = raw_xdr_qualified_name(named)

    if NAME_OVERRIDES.key?(raw_xdr_name)
      return NAME_OVERRIDES[raw_xdr_name]
    end

    xdr_name = named.name.camelize
    if NAME_OVERRIDES.key?(xdr_name)
      return NAME_OVERRIDES[xdr_name]
    end

    # Default: Xdr prefix
    if named.is_a?(AST::Concerns::NestedDefinition)
      parent = name(named.parent_defn)
      "#{parent}#{xdr_name}"
    else
      "Xdr#{xdr_name}"
    end
  end

  def raw_xdr_qualified_name(named)
    xdr_name = named.name.camelize
    if named.is_a?(AST::Concerns::NestedDefinition)
      parent_raw = raw_xdr_qualified_name(named.parent_defn)
      "#{parent_raw}#{xdr_name}"
    else
      xdr_name
    end
  end

