  def render_array_typedef(php_name, decl)
    element_typespec = decl.type
    element_type = php_type_for_typespec(element_typespec)
    is_base = BASE_WRAPPER_TYPES.include?(php_name)
    class_name = is_base ? "#{php_name}Base" : php_name
    file = file_name(class_name)
    out = @output.open(file)

    field_name = underscore_field(php_name)

    out.puts GENERATED_HEADER
    out.puts ""
    out.puts "class #{class_name} {"
    out.puts ""
    out.puts "    /** @var #{element_type}[] */"
    out.puts "    public array $#{field_name};"
    out.puts ""
    out.puts "    public function __construct(array $#{field_name}) {"
    out.puts "        $this->#{field_name} = $#{field_name};"
    out.puts "    }"
    out.puts ""
    out.puts "    public function encode(): string {"

    if decl.fixed?
      size = resolve_size(decl)
      out.puts "        $bytes = \"\";"
      out.puts "        $size = #{size};"
      out.puts "        for ($i = 0; $i < $size; $i++) {"
      out.puts "            $bytes .= #{encode_call_for_typespec(element_typespec, "$this->#{field_name}[$i]")};"
      out.puts "        }"
    else
      out.puts "        $count = count($this->#{field_name});"
      out.puts "        $bytes = XdrEncoder::integer32($count);"
      out.puts "        foreach ($this->#{field_name} as $item) {"
      out.puts "            $bytes .= #{encode_call_for_typespec(element_typespec, '$item')};"
      out.puts "        }"
    end

    out.puts "        return $bytes;"
    out.puts "    }"
    out.puts ""
    out.puts "    public static function decode(XdrBuffer $xdr): #{class_name} {"
    out.puts "        $#{field_name} = [];"

    if decl.fixed?
      size = resolve_size(decl)
      out.puts "        $size = #{size};"
      out.puts "        for ($i = 0; $i < $size; $i++) {"
      out.puts "            $#{field_name}[] = #{decode_call_for_typespec(element_typespec)};"
      out.puts "        }"
    else
      out.puts "        $size = $xdr->readInteger32();"
      out.puts "        for ($i = 0; $i < $size; $i++) {"
      out.puts "            $#{field_name}[] = #{decode_call_for_typespec(element_typespec)};"
      out.puts "        }"
    end

    out.puts "        return new #{class_name}($#{field_name});"
    out.puts "    }"
    out.puts ""
    render_base64_methods(out)
    out.puts "}"
    out.close
  end

  # ---------------------------------------------------------------------------
  # Field encode/decode helpers for struct fields
  # ---------------------------------------------------------------------------

  def render_encode_field_php(out, accessor, field_info, prefix)
    decl = field_info[:decl]
    member = field_info[:member]
    is_optional = field_info[:is_optional]

    # When a FIELD_TYPE_OVERRIDE changes the type, dispatch to that type's encode
    if field_info[:type_overridden]
      type_str = field_info[:php_type]
      if is_optional
        out.puts "        if (#{accessor} !== null) {"
        out.puts "            #{prefix} XdrEncoder::integer32(1);"
        out.puts "            $bytes .= #{encode_type_call(type_str, "#{accessor}")};"
        out.puts "        } else {"
        out.puts "            #{prefix} XdrEncoder::integer32(0);"
        out.puts "        }"
      else
        out.puts "        #{prefix} #{encode_type_call(type_str, accessor)};"
      end
      return
    end

    case decl
    when AST::Declarations::Array
      element_typespec = decl.type
      element_type = php_type_for_typespec(element_typespec)
      if decl.fixed?
        size = resolve_size(decl)
        out.puts "        #{prefix} \"\";"
        out.puts "        for ($i = 0; $i < #{size}; $i++) {"
        out.puts "            $bytes .= #{encode_type_call(element_type, "#{accessor}[$i]", typespec: element_typespec)};"
        out.puts "        }"
      else
        out.puts "        $#{field_info[:name]}Count = count(#{accessor});"
        out.puts "        #{prefix} XdrEncoder::integer32($#{field_info[:name]}Count);"
        out.puts "        foreach (#{accessor} as $#{field_info[:name]}Item) {"
        out.puts "            $bytes .= #{encode_type_call(element_type, "$#{field_info[:name]}Item", typespec: element_typespec)};"
        out.puts "        }"
      end
    when AST::Declarations::Opaque
      if decl.fixed?
        size = resolve_size(decl)
        out.puts "        #{prefix} XdrEncoder::opaqueFixed(#{accessor}, #{size});"
      else
        out.puts "        #{prefix} XdrEncoder::opaqueVariable(#{accessor});"
      end
    when AST::Declarations::String
      if is_optional
        out.puts "        if (#{accessor} !== null) {"
        out.puts "            #{prefix} XdrEncoder::integer32(1);"
        out.puts "            $bytes .= XdrEncoder::string(#{accessor});"
        out.puts "        } else {"
        out.puts "            #{prefix} XdrEncoder::integer32(0);"
        out.puts "        }"
      else
        out.puts "        #{prefix} XdrEncoder::string(#{accessor});"
      end
    else
      typespec = decl.respond_to?(:type) ? decl.type : nil
      type_str = field_info[:php_type]
      if is_optional
        out.puts "        if (#{accessor} !== null) {"
        out.puts "            #{prefix} XdrEncoder::integer32(1);"
        out.puts "            $bytes .= #{encode_type_call(type_str, "#{accessor}", typespec: typespec)};"
        out.puts "        } else {"
        out.puts "            #{prefix} XdrEncoder::integer32(0);"
        out.puts "        }"
      else
        out.puts "        #{prefix} #{encode_type_call(type_str, accessor, typespec: typespec)};"
      end
    end
  end

  def render_decode_field_php(out, local_name, field_info)
    decl = field_info[:decl]
    is_optional = field_info[:is_optional]

    # When a FIELD_TYPE_OVERRIDE changes the type, use that type's decode
    if field_info[:type_overridden]
      type_str = field_info[:php_type]
      if is_optional
        out.puts "        $#{local_name} = null;"
        out.puts "        if ($xdr->readInteger32() !== 0) {"
        out.puts "            $#{local_name} = #{decode_type_call(type_str)};"
        out.puts "        }"
      else
        out.puts "        $#{local_name} = #{decode_type_call(type_str)};"
      end
      return
    end

    case decl
    when AST::Declarations::Array
      element_typespec = decl.type
      element_type = php_type_for_typespec(element_typespec)
      out.puts "        $#{local_name} = [];"
      if decl.fixed?
        size = resolve_size(decl)
        out.puts "        for ($i = 0; $i < #{size}; $i++) {"
      else
        out.puts "        $#{local_name}Size = $xdr->readInteger32();"
        out.puts "        for ($i = 0; $i < $#{local_name}Size; $i++) {"
      end
      out.puts "            $#{local_name}[] = #{decode_type_call(element_type, typespec: element_typespec)};"
      out.puts "        }"
    when AST::Declarations::Opaque
      if decl.fixed?
        size = resolve_size(decl)
        out.puts "        $#{local_name} = $xdr->readOpaqueFixed(#{size});"
      else
        out.puts "        $#{local_name} = $xdr->readOpaqueVariable();"
      end
    when AST::Declarations::String
      if is_optional
        out.puts "        $#{local_name} = null;"
        out.puts "        if ($xdr->readInteger32() !== 0) {"
        out.puts "            $#{local_name} = $xdr->readString();"
        out.puts "        }"
      else
        out.puts "        $#{local_name} = $xdr->readString();"
      end
    else
      typespec = decl.respond_to?(:type) ? decl.type : nil
      type_str = field_info[:php_type]
      if is_optional
        out.puts "        $#{local_name} = null;"
        out.puts "        if ($xdr->readInteger32() !== 0) {"
        out.puts "            $#{local_name} = #{decode_type_call(type_str, typespec: typespec)};"
        out.puts "        }"
      else
        out.puts "        $#{local_name} = #{decode_type_call(type_str, typespec: typespec)};"
      end
    end
  end

