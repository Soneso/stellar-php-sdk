  def resolve_field_name(type_name, xdr_field_name)
    field = xdr_field_name.to_s
    if FIELD_OVERRIDES.key?(type_name) && FIELD_OVERRIDES[type_name].key?(field)
      FIELD_OVERRIDES[type_name][field]
    else
      safe_field_name(field)
    end
  end

  def resolve_member_name(type_name, xdr_member_name)
    if MEMBER_OVERRIDES.key?(type_name) && MEMBER_OVERRIDES[type_name].key?(xdr_member_name)
      MEMBER_OVERRIDES[type_name][xdr_member_name]
    elsif MEMBER_PREFIX_STRIP.key?(type_name)
      prefix = MEMBER_PREFIX_STRIP[type_name]
      xdr_member_name.delete_prefix(prefix)
    else
      xdr_member_name
    end
  end

  def resolve_factory_name(type_name, member_name)
    if FACTORY_NAME_OVERRIDES.key?(type_name) &&
       FACTORY_NAME_OVERRIDES[type_name].key?(member_name)
      FACTORY_NAME_OVERRIDES[type_name][member_name]
    elsif FACTORY_PREFIX_STRIP.key?(type_name)
      prefix = FACTORY_PREFIX_STRIP[type_name]
      member_name.delete_prefix(prefix)
    else
      member_name
    end
  end

  def safe_field_name(name)
    # PHP reserved words can still be used as property names in most contexts,
    # but we suffix with _ to be safe for the most problematic cases.
    PHP_RESERVED_WORDS.include?(name) ? "#{name}_" : name
  end

  # ---------------------------------------------------------------------------
  # Extension point helpers
  # ---------------------------------------------------------------------------

  def extension_point_field?(type_name, field_name)
    return false unless EXTENSION_POINT_FIELDS.key?(type_name)
    EXTENSION_POINT_FIELDS[type_name].include?(field_name)
  end

  # ---------------------------------------------------------------------------
  # Typedef type resolution
  # ---------------------------------------------------------------------------

  def resolve_typedef_type(underlying)
    case underlying
    when AST::Typespecs::Int
      {
        php_type: "int",
        encode: ->(field) { "XdrEncoder::integer32($this->#{field})" },
        decode: -> { "$xdr->readInteger32()" },
      }
    when AST::Typespecs::UnsignedInt
      {
        php_type: "int",
        encode: ->(field) { "XdrEncoder::unsignedInteger32($this->#{field})" },
        decode: -> { "$xdr->readUnsignedInteger32()" },
      }
    when AST::Typespecs::Hyper
      {
        php_type: "int",
        encode: ->(field) { "XdrEncoder::integer64($this->#{field})" },
        decode: -> { "$xdr->readInteger64()" },
      }
    when AST::Typespecs::UnsignedHyper
      {
        php_type: "int",
        encode: ->(field) { "XdrEncoder::unsignedInteger64($this->#{field})" },
        decode: -> { "$xdr->readUnsignedInteger64()" },
      }
    when AST::Typespecs::Bool
      {
        php_type: "bool",
        encode: ->(field) { "XdrEncoder::boolean($this->#{field})" },
        decode: -> { "$xdr->readBoolean()" },
      }
    when AST::Typespecs::Simple
      resolved = underlying.resolved_type
      if resolved.is_a?(AST::Definitions::Typedef)
        inner_decl = resolved.declaration
        # Recurse through simple typedef chains (but not opaque/string/array)
        if inner_decl.respond_to?(:type) &&
           !inner_decl.is_a?(AST::Declarations::Opaque) &&
           !inner_decl.is_a?(AST::Declarations::String) &&
           !inner_decl.is_a?(AST::Declarations::Array)
          return resolve_typedef_type(inner_decl.type)
        end
      end
      # Named type reference
      php_name = name(resolved)
      if TYPE_OVERRIDES.key?(php_name)
        override = TYPE_OVERRIDES[php_name]
        return {
          php_type: override,
          encode: ->(field) { "#{encode_type_call(override, "$this->#{field}")}" },
          decode: -> { decode_type_call(override) },
        }
      end
      {
        php_type: php_name,
        encode: ->(field) { "$this->#{field}->encode()" },
        decode: -> { "#{php_name}::decode($xdr)" },
      }
    else
      nil
    end
  end

  # ---------------------------------------------------------------------------
  # Helpers
  # ---------------------------------------------------------------------------

  def typedef_is_optional?(type)
    return false unless type.is_a?(AST::Typespecs::Simple)
    resolved = type.resolved_type
    return false unless resolved.is_a?(AST::Definitions::Typedef)
    resolved_name = name(resolved)
    return false if TYPE_OVERRIDES.key?(resolved_name)
    resolved.declaration.type.sub_type == :optional
  rescue StandardError
    false
  end

  def resolve_size(decl)
    case decl
    when AST::Declarations::Opaque
      decl.size
    when AST::Declarations::Array
      _, size = decl.type.array_size
      size
    else
      decl.size
    end
  end

  # Returns the fixed size N when typespec is a typedef that wraps opaque[N],
  # otherwise returns nil. Ported from the Flutter generator.
  def fixed_opaque_typedef_size(typespec)
    return nil unless typespec.is_a?(AST::Typespecs::Simple)
    return nil unless typespec.respond_to?(:resolved_type)
    resolved = typespec.resolved_type
    return nil unless resolved.is_a?(AST::Definitions::Typedef)
    decl = resolved.declaration
    return nil unless decl.is_a?(AST::Declarations::Opaque) && decl.fixed?
    resolve_size(decl)
  end

  # Generate a field name from class name for typedefs
  # XdrUint32 -> uint32, XdrHash -> hash, XdrString32 -> string32
  def underscore_field(class_name)
    short = class_name.sub(/\AXdr/, '')
    short[0].downcase + short[1..]
  end

