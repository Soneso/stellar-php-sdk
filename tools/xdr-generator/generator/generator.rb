  def render_array_typedef(php_name, decl)
    element_typespec = decl.type
    element_type = php_type_for_typespec(element_typespec)
    _, class_name, return_type, new_expr = resolve_class_info(php_name)
    file = file_name(class_name)
    out = @output.open(file)

    field_name = underscore_field(php_name)
    # Apply per-field name override for array typedefs
    if FIELD_OVERRIDES.key?(php_name) && FIELD_OVERRIDES[php_name].key?(field_name)
      field_name = FIELD_OVERRIDES[php_name][field_name]
    end
    is_base = BASE_WRAPPER_TYPES.include?(php_name)

    imports = []
    if sep51_needs_strkey?(php_name, is_base)
      imports << "use Soneso\\StellarSDK\\Crypto\\StrKey;"
    end

    out.puts GENERATED_HEADER
    unless imports.empty?
      out.puts ""
      imports.each { |imp| out.puts imp }
    end
    out.puts ""
    out.puts "class #{class_name} {"
    out.puts ""
    out.puts "    /** @var #{element_type}[] */"
    out.puts "    public array $#{field_name};"
    out.puts ""
    out.puts "    public function __construct(array $#{field_name}) {"
    out.puts "        $this->#{field_name} = $#{field_name};"
    out.puts "    }"
    out.puts ""
    out.puts "    public function encode(): string {"

    if decl.fixed?
      size = resolve_size(decl)
      out.puts "        $bytes = \"\";"
      out.puts "        $size = #{size};"
      out.puts "        for ($i = 0; $i < $size; $i++) {"
      out.puts "            $bytes .= #{encode_call_for_typespec(element_typespec, "$this->#{field_name}[$i]")};"
      out.puts "        }"
    else
      out.puts "        $count = count($this->#{field_name});"
      out.puts "        $bytes = XdrEncoder::integer32($count);"
      out.puts "        foreach ($this->#{field_name} as $item) {"
      out.puts "            $bytes .= #{encode_call_for_typespec(element_typespec, '$item')};"
      out.puts "        }"
    end

    out.puts "        return $bytes;"
    out.puts "    }"
    out.puts ""
    out.puts "    public static function decode(XdrBuffer $xdr): #{return_type} {"
    out.puts "        $#{field_name} = [];"

    if decl.fixed?
      size = resolve_size(decl)
      out.puts "        $size = #{size};"
      out.puts "        for ($i = 0; $i < $size; $i++) {"
      out.puts "            $#{field_name}[] = #{decode_call_for_typespec(element_typespec)};"
      out.puts "        }"
    else
      out.puts "        $size = $xdr->readInteger32();"
      out.puts "        for ($i = 0; $i < $size; $i++) {"
      out.puts "            $#{field_name}[] = #{decode_call_for_typespec(element_typespec)};"
      out.puts "        }"
    end

    out.puts "        return #{new_expr}($#{field_name});"
    out.puts "    }"
    out.puts ""
    render_base64_methods(out)
    if emits_sep51?(php_name, is_base)
      if StellarJsonOverrides.has?(php_name)
        render_overridden_sep51_methods(out, php_name, class_name)
      else
        render_array_typedef_sep51_methods(out, php_name, field_name, element_type, element_typespec, decl)
      end
    end
    if should_generate_txrep?(php_name)
      render_array_typedef_txrep_methods(out, php_name, class_name, return_type, field_name, element_type, decl)
    end
    out.puts "}"
    out.close
  end

