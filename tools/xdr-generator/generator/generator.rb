# PHP XDR code generator for the Stellar PHP SDK.
#
# This generator is invoked by xdrgen and produces PHP classes that match
# the existing hand-written XDR types in Soneso/StellarSDK/Xdr/.
#
# Usage:
#   cd tools/xdr-generator && bundle exec ruby generate.rb

require 'set'
require 'xdrgen'
require_relative 'name_overrides'
require_relative 'member_overrides'
require_relative 'field_overrides'
require_relative 'type_overrides'
require_relative 'txrep_types'
require_relative 'cat_a_inline_targets'
require_relative 'json_helpers'
require_relative 'sep51_field_overrides'
require_relative 'stellar_json_overrides'

AST = Xdrgen::AST

class Generator < Xdrgen::Generators::Base

  # PHP reserved words that cannot be used as identifiers in certain contexts.
  PHP_RESERVED_WORDS = %w[
    abstract array as break callable case catch class clone const continue
    declare default do echo else elseif empty enum eval exit extends final
    finally fn for foreach function global goto if implements include
    instanceof insteadof interface isset list match namespace new null or
    print private protected public readonly require return static switch
    throw trait try unset use var while xor yield true false int float
    string bool void never mixed
  ].freeze

  GENERATED_HEADER = <<~HEADER.freeze
    <?php declare(strict_types=1);

    // This file was automatically generated by xdrgen.
    // DO NOT EDIT or your changes may be overwritten.

    namespace Soneso\\StellarSDK\\Xdr;
  HEADER

  # ---------------------------------------------------------------------------
  # Entry point -- called by xdrgen after parsing all .x files.
  # ---------------------------------------------------------------------------

  def generate
    @generated_files = Set.new
    render_definitions(@top)
  end

  private

  # ---------------------------------------------------------------------------
  # Definition traversal
  # ---------------------------------------------------------------------------

  def render_definitions(node)
    node.definitions.each { |defn| render_definition(defn) }
    node.namespaces.each { |ns| render_definitions(ns) }
  end

  def render_nested_definitions(defn)
    return unless defn.respond_to?(:nested_definitions)
    defn.nested_definitions.each { |nested| render_definition(nested) }
  end

  def render_definition(defn)
    render_nested_definitions(defn)

    defn_name = name(defn)
    return if SKIP_TYPES.include?(defn_name)
    return if defn.is_a?(AST::Definitions::Const)

    return if @generated_files.include?(defn_name)
    @generated_files.add(defn_name)

    case defn
    when AST::Definitions::Struct
      render_struct(defn)
    when AST::Definitions::Enum
      render_enum(defn)
    when AST::Definitions::Union
      render_union(defn)
    when AST::Definitions::Typedef
      render_typedef(defn)
    end
  end

  # ---------------------------------------------------------------------------
  # Enum renderer
  #
  # PHP pattern:
  #   class XdrAssetType {
  #     private int $value;
  #     const ASSET_TYPE_NATIVE = 0;
  #     ...
  #     public function __construct(int $value) { ... }
  #     public function getValue(): int { ... }
  #     public function encode(): string { ... }
  #     public static function decode(XdrBuffer $xdr): XdrAssetType { ... }
  #   }
  # ---------------------------------------------------------------------------

  def render_enum(enum_defn)
    php_name = name(enum_defn)
    is_base, class_name, return_type, new_call = resolve_class_info(php_name)
    file = file_name(class_name)
    out = @output.open(file)

    out.puts GENERATED_HEADER
    out.puts ""
    out.puts "class #{class_name} {"
    out.puts "    public int $value;"
    out.puts ""

    enum_defn.members.each do |m|
      member_name = resolve_member_name(php_name, m.name.to_s)
      out.puts "    const #{member_name} = #{m.value};"
    end

    out.puts ""
    out.puts "    public function __construct(int $value) {"
    out.puts "        $this->value = $value;"
    out.puts "    }"
    out.puts ""
    out.puts "    public function getValue(): int {"
    out.puts "        return $this->value;"
    out.puts "    }"
    out.puts ""

    # Factory methods — one per enum constant
    enum_defn.members.each do |m|
      member_name = resolve_member_name(php_name, m.name.to_s)
      factory_name = resolve_factory_name(php_name, member_name)
      out.puts "    public static function #{factory_name}(): #{return_type} {"
      out.puts "        return #{new_call}(#{class_name}::#{member_name});"
      out.puts "    }"
      out.puts ""
    end

    # Extra factory aliases (backward compatibility)
    if FACTORY_ALIASES.key?(php_name)
      FACTORY_ALIASES[php_name].each do |alias_name, const_name|
        out.puts "    public static function #{alias_name}(): #{return_type} {"
        out.puts "        return #{new_call}(#{class_name}::#{const_name});"
        out.puts "    }"
        out.puts ""
      end
    end

    out.puts "    public function encode(): string {"
    out.puts "        return XdrEncoder::integer32($this->value);"
    out.puts "    }"
    out.puts ""
    out.puts "    public static function decode(XdrBuffer $xdr): #{return_type} {"
    out.puts "        $value = $xdr->readInteger32();"
    out.puts "        switch ($value) {"
    enum_defn.members.each do |m|
      out.puts "            case #{m.value}:"
    end
    out.puts "                return #{new_call}($value);"
    out.puts "            default:"
    out.puts "                throw new \\InvalidArgumentException(\"Unknown enum value: $value\");"
    out.puts "        }"
    out.puts "    }"
    out.puts ""
    render_base64_methods(out)
    if emits_sep51?(php_name, is_base)
      if StellarJsonOverrides.has?(php_name)
        # Per-type override: replaces the standard enum-emission template.
        # Used for enums that need bespoke handling (e.g. wrapper-extended
        # enums whose wrapper introduces additional constants the base
        # template does not know about).
        render_overridden_sep51_methods(out, php_name, class_name)
      elsif enum_uses_simple_identifier_members?(php_name, enum_defn)
        render_enum_sep51_methods(out, php_name, class_name, enum_defn)
      end
    end
    if should_generate_txrep?(php_name)
      render_enum_txrep_methods(out, php_name, class_name, enum_defn)
    end
    out.puts "}"
    out.close
  end

  # ---------------------------------------------------------------------------
  # Struct renderer
  #
  # PHP pattern:
  #   class XdrPrice {
  #     public int $n;
  #     public int $d;
  #     public function __construct(int $n, int $d) { ... }
  #     public function encode(): string { ... }
  #     public static function decode(XdrBuffer $xdr): XdrPrice { ... }
  #   }
  # ---------------------------------------------------------------------------

  def render_struct(struct)
    struct_name = name(struct)
    is_base, class_name, _, _ = resolve_class_info(struct_name)
    file = file_name(class_name)
    out = @output.open(file)

    # Collect field info
    fields = collect_struct_fields(struct, struct_name)

    has_big_integer = fields.any? { |f| f[:php_type] == "BigInteger" }
    needs_strkey = sep51_needs_strkey?(struct_name, is_base, fields: fields)
    out.puts GENERATED_HEADER
    if has_big_integer
      out.puts ""
      out.puts "use phpseclib3\\Math\\BigInteger;"
    end
    if needs_strkey
      out.puts ""
      out.puts "use Soneso\\StellarSDK\\Crypto\\StrKey;"
    end
    out.puts ""
    out.puts "class #{class_name} {"
    out.puts ""

    # Properties
    fields.each do |f|
      if f[:is_ext_point]
        out.puts "    public int $#{f[:name]} = 0;"
      elsif f[:is_optional]
        out.puts "    public ?#{f[:php_type]} $#{f[:name]} = null;"
      elsif f[:is_array]
        out.puts "    public array $#{f[:name]};"
      else
        out.puts "    public #{f[:php_type]} $#{f[:name]};"
      end
    end

    out.puts ""

    # Constructor — ext point fields excluded (they have default values)
    # PHP 8.1+: optional params (with defaults) must not precede required params.
    # Fix: reorder so required params come first, optional params last.
    constructor_fields = fields.reject { |f| f[:is_ext_point] }
    required_fields = constructor_fields.reject { |f| f[:is_optional] }
    optional_fields = constructor_fields.select { |f| f[:is_optional] }
    ordered_fields = required_fields + optional_fields

    constructor_params = ordered_fields.map { |f|
      if f[:is_optional]
        "?#{f[:php_type]} $#{f[:name]} = null"
      elsif f[:is_array]
        "array $#{f[:name]}"
      else
        "#{f[:php_type]} $#{f[:name]}"
      end
    }.join(", ")
    out.puts "    public function __construct(#{constructor_params}) {"
    ordered_fields.each do |f|
      out.puts "        $this->#{f[:name]} = $#{f[:name]};"
    end
    out.puts "    }"
    out.puts ""

    # encode()
    render_struct_encode(out, struct_name, class_name, fields)
    out.puts ""

    # decode()
    render_struct_decode(out, struct_name, class_name, fields, is_base)
    out.puts ""

    # Getters and setters for backward compatibility
    render_struct_accessors(out, fields)

    render_base64_methods(out)
    if emits_sep51?(struct_name, is_base)
      if StellarJsonOverrides.has?(struct_name)
        render_overridden_sep51_methods(out, struct_name, class_name)
      else
        render_struct_sep51_methods(out, struct_name, class_name, fields)
      end
    end
    if should_generate_txrep?(struct_name)
      render_struct_txrep_methods(out, struct_name, class_name, fields)
    end
    out.puts "}"
    out.close
  end

  def render_struct_accessors(out, fields)
    fields.each do |f|
      next if f[:is_ext_point]
      fname = f[:name]
      uc_name = "#{fname[0].upcase}#{fname[1..]}"

      if f[:is_optional]
        type_hint = "?#{f[:php_type]}"
      elsif f[:is_array]
        type_hint = "array"
      else
        type_hint = f[:php_type]
      end

      # Boolean fields starting with "is" use the field name directly as getter (Java Bean convention)
      if f[:php_type] == "bool" && fname.start_with?("is") && fname.length > 2
        out.puts "    public function #{fname}(): bool { return $this->#{fname}; }"
      else
        out.puts "    public function get#{uc_name}(): #{type_hint} { return $this->#{fname}; }"
      end
      out.puts "    public function set#{uc_name}(#{type_hint} $#{fname}): void { $this->#{fname} = $#{fname}; }"
    end
    out.puts ""
  end

  def collect_struct_fields(struct, struct_name)
    struct.members.map do |m|
      xdr_field_name = m.name.to_s
      field_name = resolve_field_name(struct_name, xdr_field_name)
      decl = m.declaration

      # Check if this is an extension point field
      is_ext = extension_point_field?(struct_name, xdr_field_name)

      # Determine type and encoding metadata
      is_optional = !is_ext && (m.type.sub_type == :optional || typedef_is_optional?(decl.type))
      type_overridden = false
      php_type = nil

      unless is_ext
        # Apply per-field type override
        if FIELD_TYPE_OVERRIDES.key?(struct_name) && FIELD_TYPE_OVERRIDES[struct_name].key?(xdr_field_name)
          php_type = FIELD_TYPE_OVERRIDES[struct_name][xdr_field_name]
          type_overridden = true
        end
        php_type ||= php_type_string(decl, m)
      end

      is_array = !is_ext && decl.is_a?(AST::Declarations::Array)

      # Inline typedef-arrays when TYPE_OVERRIDES maps them to "array".
      # This handles typedefs like LedgerEntryChanges (typedef LedgerEntryChange<>)
      # where the SDK uses a plain array instead of a wrapper class.
      if !is_ext && !is_array && php_type == "array" &&
         decl.respond_to?(:type) && decl.type.is_a?(AST::Typespecs::Simple)
        resolved = decl.type.resolved_type
        if resolved.is_a?(AST::Definitions::Typedef) &&
           resolved.declaration.is_a?(AST::Declarations::Array)
          decl = resolved.declaration
          is_array = true
        end
      end

      # For array fields whose element type is an optional typedef
      # (e.g., SponsorshipDescriptor = typedef AccountID*), the optionality
      # applies to each element, not the whole field. Track this separately.
      # Distinguish from truly optional array fields (e.g., SCMap* storage)
      # where optionality comes from m.type.sub_type == :optional.
      elements_optional = false
      if is_array && is_optional && m.type.sub_type != :optional
        is_optional = false
        elements_optional = true
      end

      {
        name: field_name,
        xdr_name: xdr_field_name,
        php_type: php_type,
        member: m,
        decl: decl,
        is_optional: is_optional,
        is_array: is_array,
        is_ext_point: is_ext,
        type_overridden: type_overridden,
        elements_optional: elements_optional,
      }
    end
  end

  def render_struct_encode(out, struct_name, class_name, fields)
    out.puts "    public function encode(): string {"
    first = true
    fields.each do |f|
      if first
        prefix = "$bytes ="
        first = false
      else
        prefix = "$bytes .="
      end
      if f[:is_ext_point]
        out.puts "        #{prefix} XdrEncoder::integer32($this->#{f[:name]});"
      else
        render_encode_field_php(out, "$this->#{f[:name]}", f, prefix)
      end
    end
    if fields.empty?
      out.puts "        $bytes = \"\";"
    end
    out.puts "        return $bytes;"
    out.puts "    }"
  end

  def render_struct_decode(out, struct_name, class_name, fields, is_base)
    _, _, return_type, _ = resolve_class_info(struct_name)
    decode_class = is_base ? "static" : struct_name
    out.puts "    public static function decode(XdrBuffer $xdr): #{return_type} {"
    fields.each do |f|
      if f[:is_ext_point]
        out.puts "        $xdr->readInteger32(); // extension point"
      else
        render_decode_field_php(out, f[:name], f)
      end
    end
    # Build constructor call using non-ext fields, reordered to match constructor
    # (required first, optional last)
    constructor_fields = fields.reject { |f| f[:is_ext_point] }
    required_fields = constructor_fields.reject { |f| f[:is_optional] }
    optional_fields = constructor_fields.select { |f| f[:is_optional] }
    ordered_fields = required_fields + optional_fields
    args = ordered_fields.map { |f| "$#{f[:name]}" }.join(", ")
    out.puts "        return new #{decode_class}(#{args});"
    out.puts "    }"
  end

  # ---------------------------------------------------------------------------
  # Union renderer
  #
  # PHP pattern (enum discriminant):
  #   class XdrAsset {
  #     public XdrAssetType $type;
  #     public ?XdrFoo $foo = null;
  #     public function __construct(XdrAssetType $type) { ... }
  #     public function encode(): string { ... }
  #     public static function decode(XdrBuffer $xdr): XdrAsset { ... }
  #   }
  # ---------------------------------------------------------------------------

  def render_union(union)
    union_name = name(union)
    is_base, class_name, _, _ = resolve_class_info(union_name)
    file = file_name(class_name)
    out = @output.open(file)

    disc_info = resolve_discriminant_info(union)
    arms = build_union_arms(union, union_name, disc_info)

    # Check if this is an extension-point-only union (void-only, simplify to int)
    # This is handled at the field level in structs via EXTENSION_POINT_FIELDS,
    # so the class itself is still generated unless SKIP_TYPES contains it.

    # Imports
    needs_bigint = arms.any? { |a| !a[:void] && a[:php_type] == "BigInteger" }
    needs_strkey = sep51_needs_strkey?(union_name, is_base, arms: arms)

    out.puts GENERATED_HEADER
    if needs_bigint
      out.puts ""
      out.puts "use phpseclib3\\Math\\BigInteger;"
    end
    if needs_strkey
      out.puts ""
      out.puts "use Soneso\\StellarSDK\\Crypto\\StrKey;"
    end
    out.puts ""
    out.puts "class #{class_name} {"
    out.puts ""

    # Discriminant field
    df = disc_info[:field_name]
    if disc_info[:kind] == :int
      out.puts "    public int $#{df};"
    else
      disc_type = disc_info[:php_name]
      out.puts "    public #{disc_type} $#{df};"
    end

    # Arm fields (nullable, with = null default)
    arms.each do |arm|
      next if arm[:void]
      out.puts "    public ?#{arm[:php_type]} $#{arm[:field_name]} = null;"
    end

    out.puts ""

    # Constructor — discriminant is optional to support no-arg construction
    # pattern used by hand-written code: new Type() → setDiscriminant() → encode().
    # This matches the existing codebase convention throughout the SDK.
    if disc_info[:kind] == :int
      out.puts "    public function __construct(?int $#{df} = null) {"
    else
      disc_type = disc_info[:php_name]
      out.puts "    public function __construct(?#{disc_type} $#{df} = null) {"
    end
    out.puts "        if ($#{df} !== null) {"
    out.puts "            $this->#{df} = $#{df};"
    out.puts "        }"
    out.puts "    }"
    out.puts ""

    # encode()
    render_union_encode(out, class_name, disc_info, arms)
    out.puts ""

    # decode()
    render_union_decode(out, union_name, class_name, disc_info, arms, is_base)
    out.puts ""

    # Getters and setters for backward compatibility
    render_union_accessors(out, disc_info, arms)

    render_base64_methods(out)
    if emits_sep51?(union_name, is_base)
      if StellarJsonOverrides.has?(union_name)
        render_overridden_sep51_methods(out, union_name, class_name)
      else
        render_union_sep51_methods(out, union_name, class_name, disc_info, arms)
      end
    end
    if should_generate_txrep?(union_name)
      render_union_txrep_methods(out, union_name, class_name, union, disc_info, arms)
    end
    out.puts "}"
    out.close
  end

  def render_union_encode(out, class_name, disc_info, arms)
    out.puts "    public function encode(): string {"
    if disc_info[:kind] == :int
      out.puts "        $bytes = XdrEncoder::integer32($this->#{disc_info[:field_name]});"
    else
      out.puts "        $bytes = $this->#{disc_info[:field_name]}->encode();"
    end

    if disc_info[:kind] == :enum
      out.puts "        switch ($this->#{disc_info[:field_name]}->getValue()) {"
    else
      out.puts "        switch ($this->#{disc_info[:field_name]}) {"
    end

    has_default = arms.any? { |a| a[:is_default] }

    arms.each do |arm|
      arm[:case_labels].each do |label|
        if label == "default"
          out.puts "            default:"
        else
          out.puts "            case #{label}:"
        end
      end

      if arm[:void]
        out.puts "                break;"
      else
        render_encode_arm_value(out, "$this->#{arm[:field_name]}", arm)
        out.puts "                break;"
      end
    end

    unless has_default
      out.puts "            default:"
      out.puts "                break;"
    end

    out.puts "        }"
    out.puts "        return $bytes;"
    out.puts "    }"
  end

  def render_union_decode(out, union_name, class_name, disc_info, arms, is_base)
    _, _, return_type, _ = resolve_class_info(union_name)
    decode_class = is_base ? "static" : union_name
    out.puts "    public static function decode(XdrBuffer $xdr): #{return_type} {"

    if disc_info[:kind] == :int
      out.puts "        $#{disc_info[:field_name]} = $xdr->readInteger32();"
      out.puts "        $result = new #{decode_class}($#{disc_info[:field_name]});"
    else
      disc_type = disc_info[:php_name]
      out.puts "        $result = new #{decode_class}(#{disc_type}::decode($xdr));"
    end

    if disc_info[:kind] == :enum
      out.puts "        switch ($result->#{disc_info[:field_name]}->getValue()) {"
    else
      out.puts "        switch ($result->#{disc_info[:field_name]}) {"
    end

    has_default = arms.any? { |a| a[:is_default] }

    arms.each do |arm|
      arm[:case_labels].each do |label|
        if label == "default"
          out.puts "            default:"
        else
          out.puts "            case #{label}:"
        end
      end

      if arm[:void]
        out.puts "                break;"
      else
        render_decode_arm_value(out, "$result", arm)
        out.puts "                break;"
      end
    end

    unless has_default
      out.puts "            default:"
      out.puts "                break;"
    end

    out.puts "        }"
    out.puts "        return $result;"
    out.puts "    }"
  end

  def render_union_accessors(out, disc_info, arms)
    # Discriminant getter/setter
    df = disc_info[:field_name]
    uc_disc = "#{df[0].upcase}#{df[1..]}"
    disc_type = disc_info[:kind] == :int ? "int" : disc_info[:php_name]
    out.puts "    public function get#{uc_disc}(): #{disc_type} { return $this->#{df}; }"
    out.puts "    public function set#{uc_disc}(#{disc_type} $#{df}): void { $this->#{df} = $#{df}; }"

    # Arm field getters/setters (nullable — arm fields are always nullable with = null default)
    arms.each do |arm|
      next if arm[:void]
      fn = arm[:field_name]
      uc_fn = "#{fn[0].upcase}#{fn[1..]}"
      type_hint = "?#{arm[:php_type]}"
      out.puts "    public function get#{uc_fn}(): #{type_hint} { return $this->#{fn}; }"
      out.puts "    public function set#{uc_fn}(#{type_hint} $#{fn}): void { $this->#{fn} = $#{fn}; }"
    end
    out.puts ""
  end

  # ---------------------------------------------------------------------------
  # Typedef renderer
  #
  # PHP typedefs map to wrapper classes:
  #   - int/uint         -> class with int field
  #   - hyper/uhyper     -> class with int field (PHP has no separate type)
  #   - opaque[N]        -> class with string field, opaqueFixed(N)
  #   - opaque<>         -> class with string field, opaqueVariable()
  #   - string<>         -> class with string field
  #   - array of T       -> class with array field
  #   - simple type ref  -> class wrapping that type
  # ---------------------------------------------------------------------------

  def render_typedef(typedef)
    php_name = name(typedef)
    # Skip typedefs that are fully replaced by TYPE_OVERRIDES
    return if TYPE_OVERRIDES.key?(php_name)

    decl = typedef.declaration

    case decl
    when AST::Declarations::Opaque
      if decl.fixed?
        size = decl.size
        render_scalar_typedef(php_name,
          ->(fn) { "XdrEncoder::opaqueFixed($this->#{fn}, #{size})" },
          "$xdr->readOpaqueFixed(#{size})",
          :opaque)
      else
        render_scalar_typedef(php_name,
          ->(fn) { "XdrEncoder::opaqueVariable($this->#{fn})" },
          "$xdr->readOpaqueVariable()",
          :opaque)
      end
    when AST::Declarations::String
      render_scalar_typedef(php_name,
        ->(fn) { "XdrEncoder::string($this->#{fn})" },
        "$xdr->readString()",
        :string)
    when AST::Declarations::Array
      render_array_typedef(php_name, decl)
    else
      render_simple_typedef(php_name, decl, typedef)
    end
  end

  def render_simple_typedef(php_name, decl, typedef)
    underlying = decl.type
    type_info = resolve_typedef_type(underlying)
    return unless type_info

    _, class_name, return_type, new_expr = resolve_class_info(php_name)
    file = file_name(class_name)
    out = @output.open(file)

    php_type = type_info[:php_type]
    field_name = type_info[:field_name] || underscore_field(php_name)
    encode_expr = type_info[:encode]
    decode_expr = type_info[:decode]

    # Apply per-field name override for typedefs
    if FIELD_OVERRIDES.key?(php_name) && FIELD_OVERRIDES[php_name].key?(field_name)
      field_name = FIELD_OVERRIDES[php_name][field_name]
    end

    # Apply per-field type override for typedefs (e.g., BigInteger)
    if FIELD_TYPE_OVERRIDES.key?(php_name) && FIELD_TYPE_OVERRIDES[php_name].key?(field_name)
      php_type = FIELD_TYPE_OVERRIDES[php_name][field_name]
      if php_type == "BigInteger"
        encode_expr = ->(fn) { "XdrEncoder::bigInteger64($this->#{fn})" }
        decode_expr = -> { "$xdr->readBigInteger64()" }
      end
    end

    is_base = BASE_WRAPPER_TYPES.include?(php_name)

    imports = []
    imports << "use phpseclib3\\Math\\BigInteger;" if php_type == "BigInteger"
    if sep51_needs_strkey?(php_name, is_base)
      imports << "use Soneso\\StellarSDK\\Crypto\\StrKey;"
    end

    out.puts GENERATED_HEADER
    unless imports.empty?
      out.puts ""
      imports.each { |imp| out.puts imp }
    end
    out.puts ""
    out.puts "class #{class_name} {"
    out.puts ""
    out.puts "    public #{php_type} $#{field_name};"
    out.puts ""
    out.puts "    public function __construct(#{php_type} $#{field_name}) {"
    out.puts "        $this->#{field_name} = $#{field_name};"
    out.puts "    }"
    out.puts ""
    out.puts "    public function encode(): string {"
    out.puts "        return #{encode_expr.call(field_name)};"
    out.puts "    }"
    out.puts ""
    out.puts "    public static function decode(XdrBuffer $xdr): #{return_type} {"
    out.puts "        return #{new_expr}(#{decode_expr.call});"
    out.puts "    }"
    out.puts ""
    render_base64_methods(out)
    if emits_sep51?(php_name, is_base)
      if StellarJsonOverrides.has?(php_name)
        render_overridden_sep51_methods(out, php_name, class_name)
      else
        render_simple_typedef_sep51_methods(out, php_name, php_type, field_name, decl)
      end
    end
    if should_generate_txrep?(php_name)
      render_simple_typedef_txrep_methods(out, php_name, class_name, return_type, php_type, field_name)
    end
    out.puts "}"
    out.close
  end

