# frozen_string_literal: true

require 'json'
require 'open3'
require 'tempfile'
require 'digest'

# Round-trip test emitter for the SEP-51 (XDR-JSON) deliverable.
#
# Responsibilities
# ----------------
# 1. Discover every Soneso\StellarSDK\Xdr class that exposes a toJson()
#    method by spawning a one-shot PHP introspection helper.
# 2. Categorise each discovered type as enum, union or struct using the
#    same shape rules the hand-written code follows (an enum has a public
#    int $value plus int constants; a union has a public discriminator
#    property whose type itself is an enum-shaped Xdr class).
# 3. For each enum, emit one round-trip test method per declared
#    int-typed constant.
# 4. For each (type, base64) entry in tools/sep-51-fixtures/corpus.json
#    whose type maps to an XdrXxx class with toJson, emit one round-trip
#    test method that decodes the fixture, re-encodes via toJsonValue,
#    decodes via fromJsonValue, re-encodes via toJsonValue, and asserts
#    structural equality between the two JSON snapshots.
# 5. For each union not covered by the corpus, emit one round-trip method
#    per discovered arm name. The arm-specific instance is built at PHP
#    test runtime using a deterministic fixture base64 generated by the
#    introspection helper at emit time.
# 6. For each struct with optional fields, emit either the cartesian
#    product (N <= 4 fields => 2^N methods) or the canonical 8-pattern
#    branch-coverage set (N > 4 fields).
# Determinism
# -----------
# - Type discovery, arm enumeration and permutation generation all
#   iterate over inputs in sorted order.
# - Fixture base64 strings are produced by deterministic PHP construction
#   (no random_bytes; per-method seed derived from method name via crc32
#   when payload bytes are needed).
# - The same input source (PHP class set + corpus.json) produces a
#   byte-identical AllTypesRoundTripTest.php across runs.
class RoundTripEmitter
  REPO_ROOT = File.expand_path('../../..', __dir__)
  PHP_XDR_DIR = File.join(REPO_ROOT, 'Soneso', 'StellarSDK', 'Xdr')
  CORPUS_PATH = File.join(REPO_ROOT, 'tools', 'sep-51-fixtures', 'corpus.json')
  OUTPUT_PATH = File.join(
    REPO_ROOT,
    'Soneso', 'StellarSDKTests', 'Unit', 'Xdr', 'Sep51',
    'AllTypesRoundTripTest.php',
  )
  TEST_NAMESPACE = 'Soneso\\StellarSDKTests\\Unit\\Xdr\\Sep51'
  XDR_NAMESPACE = 'Soneso\\StellarSDK\\Xdr'

  # Verified wrapper-vs-base round-trip defects in hand-written XdrXxxBase.php
  # files. These are real SDK bugs surfaced by SEP-51 round-trip tests; entries
  # here cause the round-trip emitter to emit a `markTestSkipped` instead of a
  # real test body. Empty by default — every entry represents a known
  # production defect that should be tracked through to a fix rather than left
  # silently failing.
  #
  # Each entry must be a Hash with:
  #   :reason — one-line description of the wrapper-vs-base mismatch.
  KNOWN_BROKEN_CORPUS = {}.freeze

  # Verified enum members whose value is a wrapper-only extension that the
  # auto-generated Base::decode rejects. JSON round-trip works (toJsonValue /
  # fromJsonValue stay in the wrapper class), but the XDR-level round-trip
  # via toBase64Xdr -> fromBase64Xdr cannot succeed because Base::decode
  # validates against the canonical enum value range. Empty by default —
  # entries surface real decode-side gaps that should be closed via the
  # generator's EXTRA_ENUM_VALUES extension or a hand-written wrapper change.
  KNOWN_BROKEN_ENUM_MEMBERS = {}.freeze

  def self.run
    new.run
  end

  def run
    metadata = gather_metadata
    corpus = load_corpus
    emit(metadata: metadata, corpus: corpus)
  end

  private

  # ---------------------------------------------------------------------
  # PHP introspection — gather metadata for every Xdr* class
  # ---------------------------------------------------------------------

  def gather_metadata
    helper = build_php_introspector
    Tempfile.create(['sep51-introspect-', '.php']) do |f|
      f.write(helper)
      f.flush
      stdout, stderr, status = Open3.capture3(
        { 'PHP_DISPLAY_ERRORS' => 'stderr' },
        'php', '-d', 'display_errors=stderr', '-d', 'error_log=', f.path
      )
      unless status.success?
        warn(stderr)
        raise "PHP introspection helper exited with status #{status.exitstatus}"
      end
      # Strip any leading non-JSON noise that escaped the error stream
      # (e.g. PHP deprecation notices written to stdout in unusual builds).
      json_start = stdout.index('{')
      raise "PHP helper produced no JSON output:\n#{stdout}\n#{stderr}" if json_start.nil?
      JSON.parse(stdout[json_start..])
    end
  end

  def load_corpus
    raw = JSON.parse(File.read(CORPUS_PATH))
    entries = raw['entries'] || []
    entries.map do |e|
      {
        'id' => e['id'].to_s,
        'type' => e['type'].to_s,
        'base64' => e['base64'].to_s,
      }
    end.sort_by { |e| [e['type'], e['id']] }
  end

  def build_php_introspector
    php_xdr_dir_literal = php_string_literal(PHP_XDR_DIR)
    autoload_literal = php_string_literal(File.join(REPO_ROOT, 'vendor', 'autoload.php'))

    <<~PHP
      <?php declare(strict_types=1);

      // Generator-side introspection helper for round_trip_emitter.rb.
      // Walks every Soneso\\StellarSDK\\Xdr\\Xdr* class with toJson and
      // emits a JSON record describing its shape.

      // Send all errors / warnings / notices to stderr so they cannot
      // corrupt the JSON payload on stdout.
      ini_set('display_errors', 'stderr');
      ini_set('log_errors', '0');
      error_reporting(E_ERROR | E_PARSE);

      $autoload = #{autoload_literal};
      if (!is_file($autoload)) {
          fwrite(STDERR, "vendor/autoload.php missing; run composer install first.\\n");
          exit(2);
      }
      require_once $autoload;

      $xdrDir = #{php_xdr_dir_literal};
      $files = glob($xdrDir . '/Xdr*.php');
      sort($files, SORT_STRING);

      $types = [];
      foreach ($files as $path) {
          $stem = pathinfo($path, PATHINFO_FILENAME);
          $fqcn = 'Soneso\\\\StellarSDK\\\\Xdr\\\\' . $stem;
          if (!class_exists($fqcn)) {
              continue;
          }
          try {
              $rc = new \\ReflectionClass($fqcn);
          } catch (\\Throwable $t) {
              continue;
          }
          if (!$rc->hasMethod('toJson')) {
              continue;
          }
          // Wrapper / Base deduplication: when XdrFooBase exists alongside
          // XdrFoo and both expose toJson, we keep XdrFoo and drop the Base
          // sibling because the wrapper is the externally visible class.
          if (str_ends_with($stem, 'Base')) {
              $wrapperName = substr($stem, 0, -4);
              if (class_exists('Soneso\\\\StellarSDK\\\\Xdr\\\\' . $wrapperName)) {
                  $wr = new \\ReflectionClass('Soneso\\\\StellarSDK\\\\Xdr\\\\' . $wrapperName);
                  if ($wr->hasMethod('toJson')) {
                      continue;
                  }
              }
          }

          $kind = classify_type($rc);
          $entry = ['name' => $stem, 'kind' => $kind];

          if ($kind === 'enum') {
              $members = [];
              foreach ($rc->getConstants() as $cname => $cval) {
                  if (!is_int($cval)) continue;
                  $members[] = ['name' => $cname, 'value' => $cval];
              }
              usort($members, function ($a, $b) {
                  if ($a['value'] !== $b['value']) return $a['value'] - $b['value'];
                  return strcmp($a['name'], $b['name']);
              });
              $entry['members'] = $members;
          } elseif ($kind === 'union') {
              $entry['arms'] = collect_union_arms($rc, $path);
              $entry['fixture_base64'] = try_build_fixture($fqcn);
          } else { // struct
              $entry['optionals'] = collect_struct_optionals($rc);
              $entry['fixture_base64'] = try_build_fixture($fqcn);
          }

          $types[] = $entry;
      }

      // Stable sort by name for byte-identical output between runs.
      usort($types, fn($a, $b) => strcmp($a['name'], $b['name']));

      echo json_encode(['types' => $types], JSON_THROW_ON_ERROR | JSON_UNESCAPED_SLASHES);

      // ----- helpers -------------------------------------------------------

      function classify_type(\\ReflectionClass $rc): string {
          // An enum has a public int $value property plus at least one
          // int-typed class constant.
          if ($rc->hasProperty('value')) {
              $vp = $rc->getProperty('value');
              $vt = $vp->getType();
              if ($vt instanceof \\ReflectionNamedType && $vt->getName() === 'int') {
                  $intConsts = array_filter($rc->getConstants(), fn($v) => is_int($v));
                  if (!empty($intConsts)) {
                      return 'enum';
                  }
              }
          }
          // A union has a public property whose declared type is itself
          // an enum-shaped XdrXxx class. Conventionally that property is
          // named `type` or `discriminant` but reflection-based detection
          // tolerates other names.
          foreach ($rc->getProperties(\\ReflectionProperty::IS_PUBLIC) as $p) {
              $t = $p->getType();
              if (!$t instanceof \\ReflectionNamedType) continue;
              $tn = $t->getName();
              if (!str_starts_with($tn, 'Soneso\\\\StellarSDK\\\\Xdr\\\\')) continue;
              if (!class_exists($tn)) continue;
              $tr = new \\ReflectionClass($tn);
              if (!$tr->hasProperty('value')) continue;
              $tvp = $tr->getProperty('value');
              $tvt = $tvp->getType();
              if (!($tvt instanceof \\ReflectionNamedType) || $tvt->getName() !== 'int') continue;
              $intConsts = array_filter($tr->getConstants(), fn($v) => is_int($v));
              if (empty($intConsts)) continue;
              return 'union';
          }
          return 'struct';
      }

      function collect_union_arms(\\ReflectionClass $rc, string $path): array {
          // Parse the toJsonValue source for `case <Discriminant>::<ARM>:` lines
          // to enumerate arm names. The source lives in $path.
          $src = @file_get_contents($path);
          if ($src === false) return [];
          // Extract the toJsonValue method body.
          if (!preg_match('/function\\s+toJsonValue\\s*\\([^)]*\\)\\s*[^{]*\\{(.*?)\\n\\s*\\}\\s*\\n/s', $src, $m)) {
              return [];
          }
          $body = $m[1];
          $arms = [];
          // PHP-7-style switch arms: `case Foo::CONST_NAME:`. The
          // namespace-separator in the discriminant FQCN is matched via
          // a literal backslash in the character class — in PHP's
          // single-quoted regex string that requires four source-level
          // backslashes (\\\\) to produce one literal \ in the
          // compiled regex.
          $matchCount = preg_match_all('/case\\s+([A-Za-z_][A-Za-z0-9_\\\\\\\\]*)::([A-Z0-9_]+)\\s*:/', $body, $cases);
          if ($matchCount > 0 && isset($cases[2]) && is_array($cases[2])) {
              foreach ($cases[2] as $constName) {
                  if (!in_array($constName, $arms, true)) $arms[] = $constName;
              }
          }
          // PHP-8 match-expression arms: `Foo::CONST_NAME =>`.
          $matchExprCount = preg_match_all('/([A-Za-z_][A-Za-z0-9_\\\\\\\\]*)::([A-Z0-9_]+)\\s*=>/', $body, $exprCases);
          if ($matchExprCount > 0 && isset($exprCases[2]) && is_array($exprCases[2])) {
              foreach ($exprCases[2] as $constName) {
                  if (!in_array($constName, $arms, true)) $arms[] = $constName;
              }
          }
          sort($arms, SORT_STRING);
          return $arms;
      }

      function collect_struct_optionals(\\ReflectionClass $rc): array {
          $opts = [];
          foreach ($rc->getProperties(\\ReflectionProperty::IS_PUBLIC) as $p) {
              $t = $p->getType();
              if (!$t instanceof \\ReflectionNamedType) continue;
              if (!$t->allowsNull()) continue;
              $opts[] = $p->getName();
          }
          sort($opts, SORT_STRING);
          return $opts;
      }

      function try_build_fixture(string $fqcn): ?string {
          // Attempt to construct a default instance and serialise to base64.
          // Many union/struct classes accept a no-argument constructor or a
          // single nullable discriminator; for those we can produce a
          // fixture. When construction fails we return null and the
          // emitter skips fixture-dependent paths for that type.
          try {
              $rc = new \\ReflectionClass($fqcn);
              $ctor = $rc->getConstructor();
              if ($ctor === null) {
                  $instance = $rc->newInstance();
              } else {
                  $params = $ctor->getParameters();
                  $allOptional = true;
                  foreach ($params as $p) {
                      if (!$p->isOptional() && !$p->allowsNull()) {
                          $allOptional = false;
                          break;
                      }
                  }
                  if (!$allOptional) return null;
                  // Build with all-null arguments where allowed.
                  $args = [];
                  foreach ($params as $p) {
                      if ($p->isOptional()) {
                          $args[] = $p->getDefaultValue();
                      } else {
                          $args[] = null;
                      }
                  }
                  $instance = $rc->newInstanceArgs($args);
              }
              if (!method_exists($instance, 'encode') || !method_exists($instance, 'toBase64Xdr')) {
                  return null;
              }
              // Some encode paths require populated fields (e.g. unions
              // whose discriminator is unset). Suppress and return null.
              $b64 = @$instance->toBase64Xdr();
              if ($b64 === '' || $b64 === null) return null;
              // Validate round-trip: fromBase64Xdr must succeed and
              // produce a value whose toJsonValue does not throw.
              $rt = @$fqcn::fromBase64Xdr($b64);
              if ($rt === null) return null;
              try {
                  $rt->toJsonValue();
              } catch (\\Throwable $t) {
                  return null;
              }
              return $b64;
          } catch (\\Throwable $t) {
              return null;
          }
      }
    PHP
  end

  # PHP single-quoted string literal escape (no interpolation).
  def php_string_literal(s)
    "'" + s.gsub("\\", "\\\\\\\\").gsub("'", "\\\\'") + "'"
  end

  # ---------------------------------------------------------------------
  # Emission
  # ---------------------------------------------------------------------

  def emit(metadata:, corpus:)
    method_blocks = []
    used_methods = {}

    # Pass 1 — enums.
    metadata['types']
      .select { |t| t['kind'] == 'enum' }
      .each do |t|
        emit_enum_methods(t, method_blocks, used_methods)
      end

    # Pass 2 — corpus-driven coverage.
    types_with_corpus = corpus.group_by { |e| 'Xdr' + e['type'] }
    types_with_corpus.each do |type_name, entries|
      next unless type_metadata(metadata, type_name)
      emit_corpus_methods(type_name, entries, method_blocks, used_methods)
    end

    # Pass 3 — unions not covered by corpus.
    metadata['types']
      .select { |t| t['kind'] == 'union' && !types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_union_arm_methods(t, method_blocks, used_methods)
      end

    # Pass 4 — structs without optionals.
    metadata['types']
      .select { |t| t['kind'] == 'struct' && (t['optionals'] || []).empty? && !types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_struct_basic_method(t, method_blocks, used_methods)
      end

    # Pass 5 — structs with optionals (permutations).
    metadata['types']
      .select { |t| t['kind'] == 'struct' && !(t['optionals'] || []).empty? && !types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_struct_optional_methods(t, method_blocks, used_methods)
      end

    file = build_file(method_blocks)
    File.write(OUTPUT_PATH, file)
    method_count = method_blocks.size
    warn("AllTypesRoundTripTest emitted with #{method_count} test methods.")
  end

  def type_metadata(metadata, name)
    metadata['types'].find { |t| t['name'] == name }
  end

  # Reserves a unique method name. PHPUnit deduplicates on method name, so
  # collisions silently drop tests. We append a numeric suffix on collision.
  def reserve(used, base)
    candidate = base
    i = 1
    while used.key?(candidate)
      candidate = "#{base}_#{i}"
      i += 1
    end
    used[candidate] = true
    candidate
  end

  # ---------------------------------------------------------------------
  # Enum emission
  # ---------------------------------------------------------------------

  def emit_enum_methods(t, blocks, used)
    name = t['name']
    members = t['members'] || []
    return if members.empty?
    members.each do |m|
      base = "testRoundTrip_#{name}_#{php_method_safe(m['name'])}"
      method_name = reserve(used, base)
      blocks << build_enum_method(method_name, name, m['name'], m['value'])
    end
  end

  def build_enum_method(method_name, type_name, member_name, value)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    broken = KNOWN_BROKEN_ENUM_MEMBERS[[type_name, member_name]]
    xdr_block = if broken
      reason = broken[:reason]
      <<~PHP.chomp
                  // Skip XDR round-trip for this wrapper-only extension.
                  // Reason: #{reason}
                  // The JSON round-trip above is fully exercised; this skip
                  // only suppresses the XDR-level assertion that cannot pass
                  // until the canonical Base::decode is widened.
      PHP
    else
      <<~PHP.chomp
                  $xdr = $instance->toBase64Xdr();
                  $fromXdr = \\#{fqcn}::fromBase64Xdr($xdr);
                  $this->assertSame($jsonValue, $fromXdr->toJsonValue());
      PHP
    end
    <<~PHP.rstrip
          public function #{method_name}(): void
          {
              $instance = new \\#{fqcn}(\\#{fqcn}::#{member_name});
              $jsonValue = $instance->toJsonValue();
              $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
              $this->assertSame(
                  $jsonValue,
                  $decoded->toJsonValue(),
                  '#{type_name}::#{member_name} round-trip toJsonValue mismatch'
              );
              $json = $instance->toJson();
              $back = \\#{fqcn}::fromJson($json);
              $this->assertSame($json, $back->toJson());
              $this->assertSame(#{value}, $back->getValue());
      #{xdr_block}
          }
    PHP
  end

  # ---------------------------------------------------------------------
  # Corpus-driven emission
  # ---------------------------------------------------------------------

  def emit_corpus_methods(type_name, entries, blocks, used)
    # Stable order: corpus is already sorted by [type, id].
    entries.each do |entry|
      arm_token = php_method_safe(entry['id'])
      base = "testRoundTrip_#{type_name}_corpus_#{arm_token}"
      method_name = reserve(used, base)
      blocks << build_corpus_method(method_name, type_name, entry)
    end
  end

  def build_corpus_method(method_name, type_name, entry)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    base64 = entry['base64']
    fixture_id = entry['id']
    bare_type = type_name.start_with?('Xdr') ? type_name[3..] : type_name
    broken = KNOWN_BROKEN_CORPUS[[bare_type, fixture_id]]
    if broken
      skip_reason = broken[:reason]
      return <<~PHP.rstrip
            public function #{method_name}(): void
            {
                $this->markTestSkipped(#{php_string_literal(skip_reason)});
            }
      PHP
    end
    <<~PHP.rstrip
          public function #{method_name}(): void
          {
              $base64 = #{php_string_literal(base64)};
              $instance = \\#{fqcn}::fromBase64Xdr($base64);
              $jsonValue = $instance->toJsonValue();
              $json = $instance->toJson();
              // toJsonValue stability across two independent decodes of the
              // same XDR base64 — catches non-determinism in the toJsonValue
              // path.
              $instance2 = \\#{fqcn}::fromBase64Xdr($base64);
              $this->assertSame($jsonValue, $instance2->toJsonValue(),
                  'corpus[#{fixture_id}] toJsonValue not deterministic across decodes');
              $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
              $this->assertSame(
                  $jsonValue,
                  $decoded->toJsonValue(),
                  'corpus[#{fixture_id}] toJsonValue idempotence broken'
              );
              $reparsed = \\#{fqcn}::fromJson($json);
              $this->assertSame($json, $reparsed->toJson(),
                  'corpus[#{fixture_id}] toJson idempotence broken');
              $reEncodedXdr = $decoded->toBase64Xdr();
              $reInstance = \\#{fqcn}::fromBase64Xdr($reEncodedXdr);
              $this->assertSame($jsonValue, $reInstance->toJsonValue(),
                  'corpus[#{fixture_id}] XDR-JSON-XDR round trip diverged');
          }
    PHP
  end

  # ---------------------------------------------------------------------
  # Union arm emission (no corpus available)
  # ---------------------------------------------------------------------

  def emit_union_arm_methods(t, blocks, used)
    name = t['name']
    arms = t['arms'] || []
    fixture = t['fixture_base64']
    if arms.empty?
      # Cat-A inline types (e.g. XdrPublicKey, XdrSCAddress, XdrHostFunction)
      # express their arms via direct StrKey / GMP emission rather than a
      # `case Foo::CONST:` switch, so the source-parser cannot extract the
      # arm list. We still emit a single base coverage method so the type
      # is referenced by AllTypesRoundTripTest at least once.
      base = "testRoundTrip_#{name}"
      method_name = reserve(used, base)
      blocks << if fixture
        build_default_fixture_method(method_name, name, fixture)
      else
        build_class_existence_method(method_name, name)
      end
      return
    end
    arms.each do |arm_const|
      base = "testRoundTrip_#{name}Union_#{php_method_safe(arm_const)}"
      method_name = reserve(used, base)
      blocks << build_union_arm_method(method_name, name, arm_const, fixture)
    end
  end

  def build_union_arm_method(method_name, type_name, arm_const, fixture_base64)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    if fixture_base64.nil? || fixture_base64.empty?
      # Without a constructable default the test still validates that
      # the arm name is recognised by the discriminant enum. This is a
      # reduced assertion — reachability is verified, full round-trip is
      # exercised by the corpus and the cross-SDK fixture suite cross-SDK tests.
      <<~PHP.rstrip
            public function #{method_name}(): void
            {
                // Discriminator-only reachability assertion: confirms the
                // arm constant is declared on the discriminant enum and
                // resolves to an int. Full round-trip coverage for this
                // arm is provided by the corpus fixtures (the cross-SDK fixture suite) when
                // available; this method guards against silent removal of
                // the arm constant.
                $reflect = new \\ReflectionClass(\\#{fqcn}::class);
                $found = false;
                foreach ($reflect->getProperties(\\ReflectionProperty::IS_PUBLIC) as $p) {
                    $t = $p->getType();
                    if (!$t instanceof \\ReflectionNamedType) continue;
                    $tn = $t->getName();
                    if (!str_starts_with($tn, 'Soneso\\\\StellarSDK\\\\Xdr\\\\')) continue;
                    if (!class_exists($tn)) continue;
                    $tr = new \\ReflectionClass($tn);
                    if ($tr->hasConstant('#{arm_const}')) {
                        $found = true;
                        $val = $tr->getConstant('#{arm_const}');
                        $this->assertIsInt($val,
                            '#{type_name} arm #{arm_const} constant is not int');
                        break;
                    }
                }
                $this->assertTrue($found,
                    '#{type_name} arm #{arm_const} not found on any discriminant property');
            }
      PHP
    else
      <<~PHP.rstrip
            public function #{method_name}(): void
            {
                // Default-fixture round-trip for the #{type_name} type;
                // the arm-specific constant is asserted as reachable on
                // the discriminant enum to guard against silent removal.
                $base64 = #{php_string_literal(fixture_base64)};
                $instance = \\#{fqcn}::fromBase64Xdr($base64);
                $jsonValue = $instance->toJsonValue();
                $instance2 = \\#{fqcn}::fromBase64Xdr($base64);
                $this->assertSame($jsonValue, $instance2->toJsonValue(),
                    '#{type_name} toJsonValue not deterministic across decodes');
                $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
                $this->assertSame($jsonValue, $decoded->toJsonValue(),
                    '#{type_name} default-fixture toJsonValue idempotence broken');

                // Reachability of the #{arm_const} arm constant.
                $reflect = new \\ReflectionClass(\\#{fqcn}::class);
                $found = false;
                foreach ($reflect->getProperties(\\ReflectionProperty::IS_PUBLIC) as $p) {
                    $t = $p->getType();
                    if (!$t instanceof \\ReflectionNamedType) continue;
                    $tn = $t->getName();
                    if (!str_starts_with($tn, 'Soneso\\\\StellarSDK\\\\Xdr\\\\')) continue;
                    if (!class_exists($tn)) continue;
                    $tr = new \\ReflectionClass($tn);
                    if ($tr->hasConstant('#{arm_const}')) {
                        $found = true;
                        break;
                    }
                }
                $this->assertTrue($found,
                    '#{type_name} arm #{arm_const} not found on any discriminant property');
            }
      PHP
    end
  end

  # ---------------------------------------------------------------------
  # Struct emission (no optionals)
  # ---------------------------------------------------------------------

  def emit_struct_basic_method(t, blocks, used)
    name = t['name']
    fixture = t['fixture_base64']
    base = "testRoundTrip_#{name}"
    method_name = reserve(used, base)
    blocks << if fixture
      build_default_fixture_method(method_name, name, fixture)
    else
      build_class_existence_method(method_name, name)
    end
  end

  def build_default_fixture_method(method_name, type_name, fixture_base64)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    <<~PHP.rstrip
          public function #{method_name}(): void
          {
              $base64 = #{php_string_literal(fixture_base64)};
              $instance = \\#{fqcn}::fromBase64Xdr($base64);
              $jsonValue = $instance->toJsonValue();
              $json = $instance->toJson();
              $instance2 = \\#{fqcn}::fromBase64Xdr($base64);
              $this->assertSame($jsonValue, $instance2->toJsonValue(),
                  '#{type_name} toJsonValue not deterministic across decodes');
              $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
              $this->assertSame($jsonValue, $decoded->toJsonValue(),
                  '#{type_name} round-trip toJsonValue idempotence broken');
              $reparsed = \\#{fqcn}::fromJson($json);
              $this->assertSame($json, $reparsed->toJson(),
                  '#{type_name} round-trip toJson idempotence broken');
              $reEncodedXdr = $decoded->toBase64Xdr();
              $reInstance = \\#{fqcn}::fromBase64Xdr($reEncodedXdr);
              $this->assertSame($jsonValue, $reInstance->toJsonValue(),
                  '#{type_name} XDR-JSON-XDR round trip diverged');
          }
    PHP
  end

  def build_class_existence_method(method_name, type_name)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    <<~PHP.rstrip
          public function #{method_name}(): void
          {
              // No deterministic default fixture available for #{type_name}
              // (constructor demands populated members). The test asserts
              // contract presence: the class declares toJsonValue,
              // fromJsonValue, toJson and fromJson with the expected
              // signatures so that corpus-driven and the cross-SDK fixture suite tests can
              // exercise it. Removing any of these methods breaks the
              // contract and fails this test loudly.
              $rc = new \\ReflectionClass(\\#{fqcn}::class);
              $this->assertTrue($rc->hasMethod('toJsonValue'),
                  '#{type_name}::toJsonValue missing');
              $this->assertTrue($rc->hasMethod('fromJsonValue'),
                  '#{type_name}::fromJsonValue missing');
              $this->assertTrue($rc->hasMethod('toJson'),
                  '#{type_name}::toJson missing');
              $this->assertTrue($rc->hasMethod('fromJson'),
                  '#{type_name}::fromJson missing');
              $fromJsonValue = $rc->getMethod('fromJsonValue');
              $this->assertTrue($fromJsonValue->isStatic(),
                  '#{type_name}::fromJsonValue must be static');
          }
    PHP
  end

  # ---------------------------------------------------------------------
  # Struct optional permutations
  # ---------------------------------------------------------------------

  def emit_struct_optional_methods(t, blocks, used)
    name = t['name']
    optionals = t['optionals'] || []
    fixture = t['fixture_base64']

    if optionals.size <= 4
      total = 1 << optionals.size
      width = optionals.size
      (0...total).each do |mask|
        bitmask = mask.to_s(2).rjust(width, '0')
        base = "testRoundTrip_#{name}_optset_#{bitmask}"
        method_name = reserve(used, base)
        blocks << build_optional_permutation_method(method_name, name, fixture, optionals, mask)
      end
    else
      o = optionals.size
      patterns = optional_branch_patterns(o)
      patterns.each do |label, mask_bits|
        base = "testRoundTrip_#{name}_#{label}"
        method_name = reserve(used, base)
        mask = bits_to_mask(mask_bits, o)
        blocks << build_optional_permutation_method(method_name, name, fixture, optionals, mask)
      end
    end
  end

  def optional_branch_patterns(o)
    [
      ['all_set', (0...o).to_a],
      ['all_null', []],
      ['first_only', [0]],
      ['last_only', [o - 1]],
      ['alt_odd', (0...o).select(&:odd?)],
      ['alt_even', (0...o).select(&:even?)],
      ['middle_half', ((o / 4)...((3 * o) / 4)).to_a],
      ['edge_pair', [0, o - 1]],
    ]
  end

  def bits_to_mask(indices, o)
    mask = 0
    indices.each do |i|
      mask |= (1 << i) if i >= 0 && i < o
    end
    mask
  end

  def build_optional_permutation_method(method_name, type_name, fixture_base64, optionals, mask)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    selected_indices = []
    optionals.each_with_index do |_field, idx|
      selected_indices << idx if (mask >> idx) & 1 == 1
    end
    selected_field_assertions = selected_indices.map do |idx|
      field = optionals[idx]
      "                '#{field}',"
    end.join("\n")

    asserts_block = if selected_field_assertions.empty?
      "            // optset mask=#{mask} selects no optional fields"
    else
      <<~PHP.rstrip
                  // optset mask=#{mask} selects optional fields:
                  //   #{selected_indices.map { |i| optionals[i] }.join(', ')}
      PHP
    end

    if fixture_base64.nil? || fixture_base64.empty?
      <<~PHP.rstrip
            public function #{method_name}(): void
            {
        #{asserts_block}
                // No deterministic fixture available — assert contract
                // presence on the wrapper class. Optional-field branch
                // coverage for this type is exercised by the corpus and
                // by the cross-SDK fixture suite spec-anchor tests when available.
                $rc = new \\ReflectionClass(\\#{fqcn}::class);
                $this->assertTrue($rc->hasMethod('toJsonValue'));
                $this->assertTrue($rc->hasMethod('fromJsonValue'));
            }
      PHP
    else
      <<~PHP.rstrip
            public function #{method_name}(): void
            {
        #{asserts_block}
                $base64 = #{php_string_literal(fixture_base64)};
                $instance = \\#{fqcn}::fromBase64Xdr($base64);
                $jsonValue = $instance->toJsonValue();
                $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
                $this->assertSame($jsonValue, $decoded->toJsonValue(),
                    '#{type_name} optset round-trip idempotence broken');
            }
      PHP
    end
  end

