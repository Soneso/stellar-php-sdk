  def build_file(method_blocks)
    body = method_blocks.join("\n\n")
    <<~PHP
      <?php declare(strict_types=1);

      // This file was automatically generated by the SEP-51 round-trip
      // emitter (tools/xdr-generator/generator/round_trip_emitter.rb).
      // DO NOT EDIT or your changes may be overwritten.
      //
      // Regenerate via:
      //   cd tools/xdr-generator && bundle exec ruby generate.rb
      //
      // The emitter walks every Soneso\\StellarSDK\\Xdr class with toJson
      // and emits per-arm / per-permutation round-trip tests.

      namespace #{TEST_NAMESPACE};

      use PHPUnit\\Framework\\TestCase;

      class AllTypesRoundTripTest extends TestCase
      {
      #{body}
      }
    PHP
  end

  # Convert an arbitrary identifier into something legal as a PHP method
  # suffix. PHP method names follow [A-Za-z_][A-Za-z0-9_]*, so we replace
  # any other character with '_'. We also collapse runs of '_'.
  def php_method_safe(s)
    cleaned = s.gsub(/[^A-Za-z0-9_]/, '_').gsub(/_+/, '_')
    cleaned = cleaned.sub(/\A_+/, '').sub(/_+\z/, '')
    cleaned.empty? ? 'unnamed' : cleaned
  end
