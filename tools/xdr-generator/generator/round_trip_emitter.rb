  def emit(metadata:, corpus:)
    buckets = { enum: [], corpus: [], union: [], struct: [] }
    used_methods = {}

    # Pass 1 — enums.
    metadata['types']
      .select { |t| t['kind'] == 'enum' }
      .each do |t|
        emit_enum_methods(t, buckets[:enum], used_methods)
      end

    # Pass 2 — corpus-driven coverage.
    types_with_corpus = corpus.group_by { |e| 'Xdr' + e['type'] }
    types_with_corpus.each do |type_name, entries|
      next unless type_metadata(metadata, type_name)
      emit_corpus_methods(type_name, entries, buckets[:corpus], used_methods)
    end

    # Pass 3a — fill in missing union arms for unions WITH a corpus
    # entry. The corpus typically only exercises a subset of arms; this
    # pass synthesises arm-pinned fixtures for every remaining arm so
    # each declared arm has at least one round-trip test.
    metadata['types']
      .select { |t| t['kind'] == 'union' && types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_union_arm_fill_methods(t, buckets[:union], used_methods)
      end

    # Pass 3b — unions not covered by corpus.
    metadata['types']
      .select { |t| t['kind'] == 'union' && !types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_union_arm_methods(t, buckets[:union], used_methods)
      end

    # Pass 4 — structs without optionals.
    metadata['types']
      .select { |t| t['kind'] == 'struct' && (t['optionals'] || []).empty? && !types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_struct_basic_method(t, buckets[:struct], used_methods)
      end

    # Pass 5 — structs with optionals (permutations).
    metadata['types']
      .select { |t| t['kind'] == 'struct' && !(t['optionals'] || []).empty? && !types_with_corpus.key?(t['name']) }
      .each do |t|
        emit_struct_optional_methods(t, buckets[:struct], used_methods)
      end

    # Pass 6 — per-struct nested-union-field permutations. For each
    # struct with one or more non-nullable union-typed fields, emit
    # arm-pinned round-trip tests. Capped per struct so a struct with
    # many union fields cannot dominate the test count.
    metadata['types']
      .select { |t| t['kind'] == 'struct' && !((t['union_fields'] || []).empty?) }
      .each do |t|
        emit_struct_union_field_methods(t, buckets[:struct], used_methods)
      end

    counts = {}
    buckets.each do |kind, blocks|
      counts[kind] = blocks.size
      File.write(OUTPUT_FILES[kind], build_file(CLASS_NAMES[kind], blocks))
    end
    total = counts.values.sum
    warn(
      "Round-trip tests emitted: enum=#{counts[:enum]} corpus=#{counts[:corpus]} " \
      "union=#{counts[:union]} struct=#{counts[:struct]} (total=#{total})."
    )
  end

  def type_metadata(metadata, name)
    metadata['types'].find { |t| t['name'] == name }
  end

  # Reserves a unique method name. PHPUnit deduplicates on method name, so
  # collisions silently drop tests. We append a numeric suffix on collision.
  def reserve(used, base)
    candidate = base
    i = 1
    while used.key?(candidate)
      candidate = "#{base}_#{i}"
      i += 1
    end
    used[candidate] = true
    candidate
  end

  # ---------------------------------------------------------------------
  # Enum emission
  # ---------------------------------------------------------------------

  def emit_enum_methods(t, blocks, used)
    name = t['name']
    members = t['members'] || []
    return if members.empty?
    members.each do |m|
      base = "testRoundTrip_#{name}_#{php_method_safe(m['name'])}"
      method_name = reserve(used, base)
      blocks << build_enum_method(method_name, name, m['name'], m['value'])
    end
  end

  def build_enum_method(method_name, type_name, member_name, value)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    broken = KNOWN_BROKEN_ENUM_MEMBERS[[type_name, member_name]]
    xdr_lines = if broken
      reason = broken[:reason]
      [
        '// Skip XDR round-trip for this wrapper-only extension.',
        "// Reason: #{reason}",
        '// The JSON round-trip above is fully exercised; this skip',
        '// only suppresses the XDR-level assertion that cannot pass',
        '// until the canonical Base::decode is widened.',
      ]
    else
      [
        '$xdr = $instance->toBase64Xdr();',
        "$fromXdr = \\#{fqcn}::fromBase64Xdr($xdr);",
        '$this->assertSame($jsonValue, $fromXdr->toJsonValue());',
      ]
    end
    indent = ' ' * 8
    xdr_block = xdr_lines.map { |l| indent + l }.join("\n")
    <<~PHP.rstrip
          public function #{method_name}(): void
          {
              $instance = new \\#{fqcn}(\\#{fqcn}::#{member_name});
              $jsonValue = $instance->toJsonValue();
              $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
              $this->assertSame(
                  $jsonValue,
                  $decoded->toJsonValue(),
                  '#{type_name}::#{member_name} round-trip toJsonValue mismatch'
              );
              $json = $instance->toJson();
              $back = \\#{fqcn}::fromJson($json);
              $this->assertSame($json, $back->toJson());
              $this->assertSame(#{value}, $back->getValue());
      #{xdr_block}
          }
    PHP
  end

  # ---------------------------------------------------------------------
  # Corpus-driven emission
  # ---------------------------------------------------------------------

  def emit_corpus_methods(type_name, entries, blocks, used)
    # Stable order: corpus is already sorted by [type, id].
    entries.each do |entry|
      arm_token = php_method_safe(entry['id'])
      base = "testRoundTrip_#{type_name}_corpus_#{arm_token}"
      method_name = reserve(used, base)
      blocks << build_corpus_method(method_name, type_name, entry)
    end
  end

  def build_corpus_method(method_name, type_name, entry)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    base64 = entry['base64']
    fixture_id = entry['id']
    bare_type = type_name.start_with?('Xdr') ? type_name[3..] : type_name
    broken = KNOWN_BROKEN_CORPUS[[bare_type, fixture_id]]
    if broken
      skip_reason = broken[:reason]
      return <<~PHP.rstrip
            public function #{method_name}(): void
            {
                $this->markTestSkipped(#{php_string_literal(skip_reason)});
            }
      PHP
    end
    <<~PHP.rstrip
          public function #{method_name}(): void
          {
              $base64 = #{php_string_literal(base64)};
              $instance = \\#{fqcn}::fromBase64Xdr($base64);
              $jsonValue = $instance->toJsonValue();
              $json = $instance->toJson();
              // toJsonValue stability across two independent decodes of the
              // same XDR base64 — catches non-determinism in the toJsonValue
              // path.
              $instance2 = \\#{fqcn}::fromBase64Xdr($base64);
              $this->assertSame($jsonValue, $instance2->toJsonValue(),
                  'corpus[#{fixture_id}] toJsonValue not deterministic across decodes');
              $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
              $this->assertSame(
                  $jsonValue,
                  $decoded->toJsonValue(),
                  'corpus[#{fixture_id}] toJsonValue idempotence broken'
              );
              $reparsed = \\#{fqcn}::fromJson($json);
              $this->assertSame($json, $reparsed->toJson(),
                  'corpus[#{fixture_id}] toJson idempotence broken');
              $reEncodedXdr = $decoded->toBase64Xdr();
              $reInstance = \\#{fqcn}::fromBase64Xdr($reEncodedXdr);
              $this->assertSame($jsonValue, $reInstance->toJsonValue(),
                  'corpus[#{fixture_id}] XDR-JSON-XDR round trip diverged');
          }
    PHP
  end

  # ---------------------------------------------------------------------
  # Union arm emission (no corpus available)
  # ---------------------------------------------------------------------

  def emit_union_arm_methods(t, blocks, used)
    name = t['name']
    arms = t['arms'] || []
    fallback_fixture = t['fixture_base64']
    arm_fixtures = t['arm_fixtures'] || {}
    if arms.empty?
      # Cat-A inline types (e.g. XdrPublicKey, XdrSCAddress, XdrHostFunction)
      # express their arms via direct StrKey / GMP emission rather than a
      # `case Foo::CONST:` switch, so the source-parser cannot extract the
      # arm list. We still emit a single base coverage method so the type
      # is referenced by AllTypesRoundTripTest at least once.
      base = "testRoundTrip_#{name}"
      method_name = reserve(used, base)
      blocks << if fallback_fixture
        build_default_fixture_method(method_name, name, fallback_fixture)
      else
        build_class_existence_method(method_name, name)
      end
      return
    end
    arms.each do |arm_const|
      base = "testRoundTrip_#{name}Union_#{php_method_safe(arm_const)}"
      method_name = reserve(used, base)
      # Prefer the arm-specific fixture (matches the arm under test);
      # fall back to the type-level default fixture which exercises a
      # different arm but still validates the round-trip path.
      fixture = arm_fixtures[arm_const] || fallback_fixture
      blocks << build_union_arm_method(method_name, name, arm_const, fixture)
    end
  end

  # Emit one round-trip method per arm constant that the corpus did not
  # already exercise. Uses arm-specific synthesised fixtures so each
  # missing arm gets a real round-trip rather than reachability-only.
  # When a synthesised fixture is unavailable for an arm, falls back to
  # the type-level default fixture so the arm constant is still
  # referenced and the toJsonValue path is exercised.
  def emit_union_arm_fill_methods(t, blocks, used)
    name = t['name']
    arms = t['arms'] || []
    return if arms.empty?
    fallback_fixture = t['fixture_base64']
    arm_fixtures = t['arm_fixtures'] || {}
    covered = (t['corpus_covered_arms'] || []).to_set
    arms.each do |arm_const|
      next if covered.include?(arm_const)
      base = "testRoundTrip_#{name}Union_#{php_method_safe(arm_const)}"
      method_name = reserve(used, base)
      fixture = arm_fixtures[arm_const] || fallback_fixture
      blocks << build_union_arm_method(method_name, name, arm_const, fixture)
    end
  end

  def build_union_arm_method(method_name, type_name, arm_const, fixture_base64)
    fqcn = "#{XDR_NAMESPACE}\\#{type_name}"
    if fixture_base64.nil? || fixture_base64.empty?
      # Without a constructable default the test still validates that
      # the arm name is recognised by the discriminant enum. This is a
      # reduced assertion — reachability is verified, full round-trip is
      # exercised by the corpus snapshot.
      <<~PHP.rstrip
            public function #{method_name}(): void
            {
                // Discriminator-only reachability assertion: confirms the
                // arm constant is declared on the discriminant enum and
                // resolves to an int. Full round-trip coverage for this
                // arm is provided by the snapshot corpus when available;
                // this method guards against silent removal of the arm
                // constant.
                $reflect = new \\ReflectionClass(\\#{fqcn}::class);
                $found = false;
                foreach ($reflect->getProperties(\\ReflectionProperty::IS_PUBLIC) as $p) {
                    $t = $p->getType();
                    if (!$t instanceof \\ReflectionNamedType) continue;
                    $tn = $t->getName();
                    if (!str_starts_with($tn, 'Soneso\\\\StellarSDK\\\\Xdr\\\\')) continue;
                    if (!class_exists($tn)) continue;
                    $tr = new \\ReflectionClass($tn);
                    if ($tr->hasConstant('#{arm_const}')) {
                        $found = true;
                        $val = $tr->getConstant('#{arm_const}');
                        $this->assertIsInt($val,
                            '#{type_name} arm #{arm_const} constant is not int');
                        break;
                    }
                }
                $this->assertTrue($found,
                    '#{type_name} arm #{arm_const} not found on any discriminant property');
            }
      PHP
    else
      <<~PHP.rstrip
            public function #{method_name}(): void
            {
                // Default-fixture round-trip for the #{type_name} type;
                // the arm-specific constant is asserted as reachable on
                // the discriminant enum to guard against silent removal.
                $base64 = #{php_string_literal(fixture_base64)};
                $instance = \\#{fqcn}::fromBase64Xdr($base64);
                $jsonValue = $instance->toJsonValue();
                $instance2 = \\#{fqcn}::fromBase64Xdr($base64);
                $this->assertSame($jsonValue, $instance2->toJsonValue(),
                    '#{type_name} toJsonValue not deterministic across decodes');
                $decoded = \\#{fqcn}::fromJsonValue($jsonValue);
                $this->assertSame($jsonValue, $decoded->toJsonValue(),
                    '#{type_name} default-fixture toJsonValue idempotence broken');

                // Reachability of the #{arm_const} arm constant.
                $reflect = new \\ReflectionClass(\\#{fqcn}::class);
                $found = false;
                foreach ($reflect->getProperties(\\ReflectionProperty::IS_PUBLIC) as $p) {
                    $t = $p->getType();
                    if (!$t instanceof \\ReflectionNamedType) continue;
                    $tn = $t->getName();
                    if (!str_starts_with($tn, 'Soneso\\\\StellarSDK\\\\Xdr\\\\')) continue;
                    if (!class_exists($tn)) continue;
                    $tr = new \\ReflectionClass($tn);
                    if ($tr->hasConstant('#{arm_const}')) {
                        $found = true;
                        break;
                    }
                }
                $this->assertTrue($found,
                    '#{type_name} arm #{arm_const} not found on any discriminant property');
            }
      PHP
    end
  end

