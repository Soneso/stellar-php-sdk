}.freeze

# ---------------------------------------------------------------------------
# Helper method: checks whether a resolved PHP class name needs TxRep methods.
# Accepts the resolved PHP name (e.g., 'XdrPaymentOperation') and returns true
# if its XDR origin is in TXREP_XDR_NAMES or the PHP name is in TXREP_TYPES.
# ---------------------------------------------------------------------------
def should_generate_txrep?(php_name)
  TXREP_TYPES.include?(php_name)
end
