  'XdrAccountID'                => { format: 'TxRepHelper::formatAccountId',       parse: 'TxRepHelper::parseAccountId' },
  'XdrAllowTrustOperationAsset' => { format: 'TxRepHelper::formatAllowTrustAsset', parse: 'TxRepHelper::parseAllowTrustAsset' },
